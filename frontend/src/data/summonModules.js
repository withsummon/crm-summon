export const summonModuleGroups = [
  'CRM Core',
  'Lending & Risk',
  'Operations',
  'Admin & Platform',
  'Channels & Portal',
].map((group) => ({
  name: group,
  opened: group === 'CRM Core' || group === 'Lending & Risk',
  views: summonModules.filter((module) => module.group === group),
}))

export function getSummonModule(moduleSlug) {
  return summonModules.find((module) => {
    const slug = module.href?.split('/').pop()
    return slug === moduleSlug
  })
}
